import Head from "next/head";
import { useState } from "react";
import { createClient } from "redis";
import { DateRange } from "../components/DateRange";
import { SceneSelector } from "../components/SceneSelector";
import styles from "../styles/Home.module.css";

const title = "Tært Conzært";

export default function Home({ shows, scenes, allShows }) {
  const [selected, setSelected] = useState();

  const showsToRender = selected ? shows[selected] : allShows;

  return (
    <div className={styles.container}>
      <Head>
        <title>{title}</title>
        <meta name="description" content="Generated by a magician" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>{title}</h1>
        <SceneSelector
          scenes={scenes}
          selected={selected}
          setSelected={setSelected}
        />
        <DateRange />

        <div className={styles.grid}>
          {/* TODO: Display more info about shows here */}
          {showsToRender.map((show, i) => (
            <a
              key={show.title + "_" + i}
              href={show.ticketLink}
              className={styles.card}
            >
              <h2 className={styles.cardTitle}>{show.title.toUpperCase()}</h2>
              <div className={styles.cardDate}>
                <p>{show.date}</p>
              </div>
            </a>
          ))}
        </div>
      </main>

      <footer className={styles.footer}>©hansmaast</footer>
    </div>
  );
}

export async function getStaticProps() {
  const client = createClient({
    url: process.env.redis_url,
  });

  client.on("error", (err) => console.log("Redis Client Error", err));

  await client.connect();

  let allShows = [];
  let shows = await client.hGetAll("shows");
  const scenes = Object.keys(shows);

  scenes.forEach((scene) => {
    shows[scene] = JSON.parse(shows[scene]);
    allShows = [...allShows, ...shows[scene]];
  });

  await client.disconnect();

  return {
    props: {
      shows,
      scenes,
      allShows,
    },
  };
}
